// 1. 입력창을 통한 입력
// 리턴타입 : String
let dinner = prompt('저녁 뭐 먹을래요?','피자');

//dinner에 값이 잘 담겼는지 콘솔창으로 확인해보기
console.log(dinner);


// 2. 확인 및 취소를 통한 입력
// 리턴타입 : Boolean
// 확인 - True, 취소 - False

let coffee = confirm('커피도 마실래요?');
console.log(coffee);

